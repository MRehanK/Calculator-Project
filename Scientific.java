
public class Scientific extends Standard{

	
	public double sin(double rad) {
		result = Math.sin(rad);
		return result;
	}
	
	public double cos(double rad) {
		result = Math.cos(rad);
		return result;
	}
	
	public double tan(double rad) {
		result = Math.tan(rad);
		return result;
	}
}
